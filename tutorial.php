    <h2>Herbarium Preservation Techniques</h2>
    <div class="tech-con">
      <div class="tech-row">
          <h3>Storage Conditions</h3>
          <p>Keep in a cool (15-20Â°C), dry place with 40-60% relative humidity. Avoid light exposure to prevent fading.</p>
      </div>

      <div class="tech-row">
      
          <h3>Pest Control</h3>
          <p>Freeze specimens for a few days to kill insects. Use insect repellents like naphthalene or silica gel to deter pests.</p>
        
      </div>

      <div class="tech-row">
      
          <h3>Minimal Handling</h3>
          <p>Handle with gloves or tweezers to avoid transferring oils or damaging the specimens. Store them in protective covers or flat herbarium cabinets.</p>
        

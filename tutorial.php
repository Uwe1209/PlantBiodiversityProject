    <h2>4-Step-Guide</h2>
      <div class="step-con">
        <div class="step-row">
          <p>Step 1: </p>
          <p>Pick a fresh leaf.</p>
          <img src="img/Step/step_1.png" alt="step 1">
        </div>
  
          <div class="step-row">
            <p>Step: 2</p>
            <p>Place between newspaper.</p>
            <img src="img/Step/step_2.png" alt="step 2">
          </div>
  
          <div class="step-row">
            <p>Step: 3</p>
            <p>Sandwich between cardboard.</p>
            <img src="img/Step/step_3.png" alt="step 3">
          </div>
          
          <div class="step-row">
            <p>Step: 4</p>
            <p>Place under a heavy book.</p>
            <img src="img/Step/step_4.png" alt="step 4">
          </div>
  
          <div class="step-row">
            <p>Step: 5</p>
            <p>Leave for 1 week to dry.</p>
            <img src="img/Step/step_5.png" alt="step 5">
          </div>
  
          <div class="step-row">
            <p>Step: 6</p>
            <p>Remove from newspaper.</p>
            <img src="img/Step/step_6.png" alt="step 6">
          </div>
  
          <div class="step-row">
            <p>Step: 7</p>
            <p>Glue it on a paper.</p>
            <img src="img/Step/step_7.png" alt="step 7">
          </div>
  
          <div class="step-row">
            <p>Step: 8</p>
            <p>Write down the information on paper.</p>
            <img src="img/Step/step_8.png" alt="step 8">
          </div>
  
        </div>
        <p>That's it! Your herbarium specimen is ready.</p>
  </div>

        
